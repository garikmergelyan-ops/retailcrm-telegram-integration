// –§—É–Ω–∫—Ü–∏—è –¥–ª—è –æ—Ç–ø—Ä–∞–≤–∫–∏ —Å–æ–æ–±—â–µ–Ω–∏—è –≤ Telegram
async function sendTelegramMessage(message, channelId = null) {
    try {
        const botToken = process.env.TELEGRAM_BOT_TOKEN;
        const targetChannel = channelId || process.env.TELEGRAM_CHANNEL_ID;
        
        if (!botToken || !targetChannel) {
            console.error('Missing Telegram settings');
            return false;
        }

        const response = await axios.post(`https://api.telegram.org/bot${botToken}/sendMessage`, {
            chat_id: targetChannel,
            text: message,
            parse_mode: 'HTML'
        });

        console.log(`‚úÖ Message sent to Telegram channel: ${targetChannel}`);
        return true;
    } catch (error) {
        console.error('‚ùå Error sending to Telegram:', error.message);
        return false;
    }
}

// –§—É–Ω–∫—Ü–∏—è –¥–ª—è –ø–æ–ª—É—á–µ–Ω–∏—è –∑–∞–∫–∞–∑–æ–≤ –∏–∑ RetailCRM
async function getOrdersFromRetailCRM() {
    try {
        let allOrders = [];
        
        // –ü–æ–ª—É—á–∞–µ–º –∑–∞–∫–∞–∑—ã –∏–∑ –≤—Å–µ—Ö –Ω–∞—Å—Ç—Ä–æ–µ–Ω–Ω—ã—Ö –∞–∫–∫–∞—É–Ω—Ç–æ–≤
        for (const account of retailCRMAccounts) {
            try {
                console.log(`üîç Checking orders from ${account.name}...`);
                
                const response = await axios.get(`${account.url}/api/v5/orders`, {
                    params: { 
                        apiKey: account.apiKey,
                        limit: 100
                    }
                });

                if (response.data.success && response.data.orders) {
                    // –î–æ–±–∞–≤–ª—è–µ–º –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –æ–± –∞–∫–∫–∞—É–Ω—Ç–µ –∫ –∫–∞–∂–¥–æ–º—É –∑–∞–∫–∞–∑—É
                    const ordersWithAccount = response.data.orders.map(order => ({
                        ...order,
                        accountName: account.name,
                        accountUrl: account.url,
                        accountCurrency: account.currency,
                        telegramChannel: account.telegramChannel
                    }));
                    
                    allOrders = allOrders.concat(ordersWithAccount);
                    console.log(`‚úÖ Got ${response.data.orders.length} orders from ${account.name}`);
                } else {
                    console.error(`‚ùå Error getting orders from ${account.name}:`, response.data.errorMsg);
                }
            } catch (error) {
                console.error(`‚ùå Error with ${account.name}:`, error.message);
            }
        }
        
        return allOrders;
    } catch (error) {
        console.error('RetailCRM API error:', error.message);
        return [];
    }
}

// –§—É–Ω–∫—Ü–∏—è –¥–ª—è –ø–æ–ª—É—á–µ–Ω–∏—è –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏–∏ –æ–± –æ–ø–µ—Ä–∞—Ç–æ—Ä–µ –ø–æ ID
async function getManagerInfo(managerId) {
    try {
        const retailcrmUrl = process.env.RETAILCRM_URL;
        const apiKey = process.env.RETAILCRM_API_KEY;
        
        if (!retailcrmUrl || !apiKey || !managerId) {
            return null;
        }

        const response = await axios.get(`${retailcrmUrl}/api/v5/users/${managerId}`, {
            params: { apiKey }
        });

        if (response.data.success) {
            return response.data.user;
        } else {
            console.error('Error getting operator information:', response.data.errorMsg);
            return null;
        }
    } catch (error) {
        console.error('API error getting operator:', error.message);
        return null;
    }
}

// –§—É–Ω–∫—Ü–∏—è –¥–ª—è —Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏—è —Å–æ–æ–±—â–µ–Ω–∏—è –æ –∑–∞–∫–∞–∑–µ
async function formatOrderMessage(order) {
    // –ü–æ–ª—É—á–∞–µ–º –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –æ–± –æ–ø–µ—Ä–∞—Ç–æ—Ä–µ
    let managerName = '–ù–µ —É–∫–∞–∑–∞–Ω';
    if (order.managerId) {
        const manager = await getManagerInfo(order.managerId);
        if (manager) {
            managerName = manager.firstName && manager.lastName ? 
                `${manager.firstName} ${manager.lastName}` : 
                manager.firstName || manager.lastName || `ID: ${order.managerId}`;
        } else {
            managerName = `ID: ${order.managerId}`;
        }
    }

    const items = order.items || [];
    const itemsText = items.map(item => {
        const productName = item.offer?.displayName || item.offer?.name || 'Product';
        const quantity = item.quantity || 1;
        return `‚Ä¢ ${productName} - ${quantity} pcs`;
    }).join('\n');

    // –ü–æ–ª—É—á–∞–µ–º –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –æ –¥–æ—Å—Ç–∞–≤–∫–µ
    const deliveryAddress = order.delivery?.address;
    const addressText = deliveryAddress ? 
        `${deliveryAddress.street || ''} ${deliveryAddress.building || ''} ${deliveryAddress.apartment || ''}`.trim() || 
        deliveryAddress.text || 
        'Not specified' : 'Not specified';
    
    const city = deliveryAddress?.city || order.delivery?.city || 'Not specified';
    const deliveryDate = order.delivery?.date || order.deliveryDate || 'Not specified';
    
    // –ü–æ–ª—É—á–∞–µ–º –¥–æ–ø–æ–ª–Ω–∏—Ç–µ–ª—å–Ω—ã–π —Ç–µ–ª–µ—Ñ–æ–Ω
    const additionalPhone = order.additionalPhone || 
                           (order.contact?.phones && order.contact.phones.length > 1 ? 
                            order.contact.phones[1].number : 'Not specified');

    // –ü–æ–ª—É—á–∞–µ–º –≤—Ä–µ–º—è –ø–æ –ì–∞–Ω–µ (GMT+0)
    const ghanaTime = new Date().toLocaleString('en-GB', {
        timeZone: 'Africa/Accra',
        year: 'numeric',
        month: '2-digit',
        day: '2-digit',
        hour: '2-digit',
        minute: '2-digit',
        second: '2-digit'
    });

    return `üõí <b>NEW ORDER APPROVED!</b>

üìã <b>Order Number:</b> ${order.number || order.id}
üë§ <b>Operator:</b> ${managerName}
üìÖ <b>Delivery Date:</b> ${deliveryDate}
üë®‚Äçüíº <b>Customer Name:</b> ${order.firstName || ''} ${order.lastName || ''}
üì± <b>Phone:</b> ${order.phone || 'Not specified'}
üì± <b>Additional Phone:</b> ${additionalPhone}
üìç <b>Delivery Address:</b> ${addressText}
üèôÔ∏è <b>City:</b> ${city}

üõçÔ∏è <b>Products:</b>
${itemsText}

üí∞ <b>Order Total:</b> ${order.totalSumm || 0} ${process.env.CURRENCY || 'GHS'}

‚è∞ <b>Approval Time:</b> ${ghanaTime} (Ghana Time)`;
}

// –§—É–Ω–∫—Ü–∏—è –¥–ª—è –ø—Ä–æ–≤–µ—Ä–∫–∏ –∏–∑–º–µ–Ω–µ–Ω–∏–π —Å—Ç–∞—Ç—É—Å–æ–≤ –∑–∞–∫–∞–∑–æ–≤
async function checkOrderStatusChanges() {
    try {
        console.log(`üîç [${serverId}] Checking orders... (tracked: ${orderStatuses.size})`);
        
        const orders = await getOrdersFromRetailCRM();
        let newApprovalsCount = 0;
        let isFirstRun = orderStatuses.size === 0; // –ü—Ä–æ–≤–µ—Ä—è–µ–º, –ø–µ—Ä–≤—ã–π –ª–∏ —ç—Ç–æ –∑–∞–ø—É—Å–∫
        
        if (isFirstRun) {
            console.log('üöÄ First run - checking all approved orders...');
        }
        
        for (const order of orders) {
            const orderId = order.id;
            const currentStatus = order.status;
            const currentUpdate = order.updatedAt || order.dateCreate;
            
            // –ü–æ–ª—É—á–∞–µ–º –ø—Ä–µ–¥—ã–¥—É—â–∏–π —Å—Ç–∞—Ç—É—Å
            const previousData = orderStatuses.get(orderId);
            
            if (!previousData) {
                // –ü–µ—Ä–≤—ã–π —Ä–∞–∑ –≤–∏–¥–∏–º —ç—Ç–æ—Ç –∑–∞–∫–∞–∑
                orderStatuses.set(orderId, {
                    status: currentStatus,
                    lastUpdate: currentUpdate
                });
                
                // –ï—Å–ª–∏ –∑–∞–∫–∞–∑ —É–∂–µ approved, –¥–æ–±–∞–≤–ª—è–µ–º –≤ –æ—Ç—Å–ª–µ–∂–∏–≤–∞–Ω–∏–µ (–Ω–æ –Ω–µ –æ—Ç–ø—Ä–∞–≤–ª—è–µ–º —É–≤–µ–¥–æ–º–ª–µ–Ω–∏–µ –ø—Ä–∏ –ø–µ—Ä–≤–æ–º –∑–∞–ø—É—Å–∫–µ)
                if (currentStatus === 'approved') {
                    if (isFirstRun) {
                        console.log(`‚úÖ Order ${order.number || orderId} is already approved - added to tracking (first run)`);
                    } else {
                        console.log(`üÜï New approved order ${order.number || orderId} found!`);
                        const message = await formatOrderMessage(order);
                        await sendTelegramMessage(message, order.telegramChannel);
                        newApprovalsCount++;
                    }
                }
            } else {
                // –ü—Ä–æ–≤–µ—Ä—è–µ–º, –∏–∑–º–µ–Ω–∏–ª—Å—è –ª–∏ —Å—Ç–∞—Ç—É—Å
                if (previousData.status !== currentStatus) {
                    console.log(`üîÑ Order ${order.number || orderId} status changed: ${previousData.status} ‚Üí ${currentStatus}`);
                    
                    // –ï—Å–ª–∏ —Å—Ç–∞—Ç—É—Å –∏–∑–º–µ–Ω–∏–ª—Å—è –Ω–∞ approved, –æ—Ç–ø—Ä–∞–≤–ª—è–µ–º —É–≤–µ–¥–æ–º–ª–µ–Ω–∏–µ
                    if (currentStatus === 'approved') {
                        console.log(`üÜï Order ${order.number || orderId} was just approved!`);
                        
                        const message = await formatOrderMessage(order);
                        await sendTelegramMessage(message, order.telegramChannel);
                        newApprovalsCount++;
                    }
                    
                    // –û–±–Ω–æ–≤–ª—è–µ–º –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –æ –∑–∞–∫–∞–∑–µ
                    orderStatuses.set(orderId, {
                        status: currentStatus,
                        lastUpdate: currentUpdate
                    });
                }
                // –£–±–∏—Ä–∞–µ–º –ª–æ–≥–∏—Ä–æ–≤–∞–Ω–∏–µ –Ω–µ–∏–∑–º–µ–Ω–µ–Ω–Ω—ã—Ö —Å—Ç–∞—Ç—É—Å–æ–≤ –¥–ª—è —ç–∫–æ–Ω–æ–º–∏–∏ —Ä–µ—Å—É—Ä—Å–æ–≤
            }
        }
        
        if (isFirstRun) {
            console.log(`üéØ First run completed. Found ${orderStatuses.size} orders to track.`);
        } else if (newApprovalsCount > 0) {
            console.log(`‚úÖ Sent ${newApprovalsCount} approval notification(s)`);
        }
        // –£–±–∏—Ä–∞–µ–º –∏–∑–±—ã—Ç–æ—á–Ω—ã–µ –ª–æ–≥–∏ –¥–ª—è —ç–∫–æ–Ω–æ–º–∏–∏ —Ä–µ—Å—É—Ä—Å–æ–≤
        
    } catch (error) {
        console.error('‚ùå Error checking orders:', error.message);
    }
}

// –§—É–Ω–∫—Ü–∏—è –¥–ª—è –ø–æ–ª–Ω–æ–π –ø—Ä–æ–≤–µ—Ä–∫–∏ –≤—Å–µ—Ö approved –∑–∞–∫–∞–∑–æ–≤ –ø—Ä–∏ –∑–∞–ø—É—Å–∫–µ
async function checkAllApprovedOrdersOnStartup() {
    try {
        console.log('üöÄ Starting full check of all approved orders...');
        
        const orders = await getOrdersFromRetailCRM();
        let approvedOrdersFound = 0;
        
        for (const order of orders) {
            if (order.status === 'approved') {
                const orderId = order.id;
                const previousData = orderStatuses.get(orderId);
                
                if (!previousData) {
                    // –ù–æ–≤—ã–π approved –∑–∞–∫–∞–∑ - –¥–æ–±–∞–≤–ª—è–µ–º –≤ –æ—Ç—Å–ª–µ–∂–∏–≤–∞–Ω–∏–µ
                    orderStatuses.set(orderId, {
                        status: 'approved',
                        lastUpdate: order.updatedAt || order.dateCreate
                    });
                    
                    console.log(`‚úÖ Found approved order ${order.number || orderId} - added to tracking`);
                    approvedOrdersFound++;
                } else if (previousData.status !== 'approved') {
                    // –°—Ç–∞—Ç—É—Å –∏–∑–º–µ–Ω–∏–ª—Å—è –Ω–∞ approved - –æ—Ç–ø—Ä–∞–≤–ª—è–µ–º —É–≤–µ–¥–æ–º–ª–µ–Ω–∏–µ
                    console.log(`üÜï Order ${order.number || orderId} status changed to approved!`);
                    
                    const message = await formatOrderMessage(order);
                    await sendTelegramMessage(message, order.telegramChannel);
                    
                    // –û–±–Ω–æ–≤–ª—è–µ–º —Å—Ç–∞—Ç—É—Å
                    orderStatuses.set(orderId, {
                        status: 'approved',
                        lastUpdate: order.updatedAt || order.dateCreate
                    });
                    
                    approvedOrdersFound++;
                }
            }
        }
        
        console.log(`üéØ Found ${approvedOrdersFound} approved orders on startup`);
        
    } catch (error) {
        console.error('‚ùå Error checking approved orders on startup:', error.message);
    }
}

// –ó–∞–ø—É—Å–∫–∞–µ–º –ø–µ—Ä–∏–æ–¥–∏—á–µ—Å–∫—É—é –ø—Ä–æ–≤–µ—Ä–∫—É –∫–∞–∂–¥—ã–µ 30 —Å–µ–∫—É–Ω–¥
